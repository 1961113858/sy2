import numpy as np

# 创建场景数据
scene_data = np.zeros((5, 10))  # 5个家具，每个10个属性

# 添加家具数据
# 格式：[旋转x, 旋转y, 旋转z, 位置x, 位置y, 位置z, 尺寸x, 尺寸y, 尺寸z, 类别]

# 床
scene_data[0] = [0, 0, 0,  # 旋转
                 0, 0, 0,  # 位置（房间中央）
                 2.0, 1.8, 0.5,  # 尺寸
                 0]  # 类别（床）

# 床头柜
scene_data[1] = [0, 0, 0,  # 旋转
                 1.2, 0, 0,  # 位置（床的右侧）
                 0.5, 0.5, 0.5,  # 尺寸
                 1]  # 类别（床头柜）

# 衣柜
scene_data[2] = [0, 0, np.pi/2,  # 旋转（90度）
                 -1.5, 2, 0,  # 位置（房间左侧）
                 0.6, 2.0, 2.0,  # 尺寸
                 2]  # 类别（衣柜）

# 书桌
scene_data[3] = [0, 0, 0,  # 旋转
                 2, 2, 0,  # 位置（房间右上角）
                 1.2, 0.6, 0.75,  # 尺寸
                 3]  # 类别（书桌）

# 椅子
scene_data[4] = [0, 0, 0,  # 旋转
                 2, 1.5, 0,  # 位置（书桌前）
                 0.4, 0.4, 0.45,  # 尺寸
                 4]  # 类别（椅子）

# 保存场景数据
np.save("optimized_layout.npy", scene_data)
print("布局数据已保存到 optimized_layout.npy") 